/*
  ~ Function adalah blok kode program yang berjalan saat kita panggil
  ~ contohnya print()
  ~ Untuk membuat function, kita bisa menggunakan kata kucni void,
    lalu diikuti dengan nama function, kurang() dan diakhiri dengan blok
  ~ Kita bisa memanggil function dengan menggunakan nama function lalu diikuti
    dengan kurung ()
  ~ Di Bahasa Pemrograman lain, Function juga disebut dengn Method
   */

void sayHello() {
  print('Hallo World');
}

void main() {
  sayHello();
  sayHello();
  sayHello();
}
