void main() {
  /* Baca dulu !!
  While Loop adalah versi perulangan yang lebih sederhana dibanding for loop
  di While loop, hanya terdapat kondisi perulangan, tanpa ada init statement dan post statment
  
  */

  // Kode pada While Loop
  var counter = 1;

  while (counter <= 10) {
    print('Nomor $counter');
    counter++;
  }
}
