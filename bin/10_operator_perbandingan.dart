void main() {
  /*Baca Dulu

  Operator                Keterangan
  ==                      Sama Dengan
  !=                      Tidak Sama Dengan
  >                       Lebih Dari
  <                       Kurang Dari
  >=                      Lebih Dari Atau Sama Dengan
  <=                      Kurang Dari Atau Sama Dengan

  */
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 <= 10);
  print(10 >= 10);

  print('Qadri' == 'Qadri');
  print('Qadri' != 'Qadri');
}
