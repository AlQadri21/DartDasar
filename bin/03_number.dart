void main() {
  /* Baca Dulu 
  int adalah tipe data bilangan bulat
  double adalah tipe data bilangan desimal
  num dia bisa menggunakan int dan double
  */
  // Nomor Dengan Kode Int
  int number1 = 10;
  print(number1);

  // Nomor Dengan Kode Double
  double number2 = 10.5;
  print(number2);

  // Nomor Dengan Kode num
  num number3 = 10;
  num number4 = 21.2;

  print(number3);
  print(number4);
}
